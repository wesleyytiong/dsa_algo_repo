#Stack Data Structure - First-In/Last-Out (FILO) Linear Data Structure

#initializing stack as a list
stack = []

#append() to push elements in the stack
stack.append('a')
stack.append('b')
stack.append('c')
print('Initial stack is' + str(stack))

#pop() Time Complexity O(1)
print(stack.pop())
print(stack.pop())
print(stack.pop())

#print stack to check values
print('Stack after all items are popped:' + str(stack))

